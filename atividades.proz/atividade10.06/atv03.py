'''
Exercício 3: Cálculo de Área de um Retângulo
Solicite a largura e a altura de um retângulo e calcule a área.
'''
lar = float(input('Qual a largura do retângulo? '))
alt = float(input('Qual a altura do retângulo? '))
print('A área do retângulo de largura {} e comprimento {} é de {}'.format(lar, alt, alt*lar))